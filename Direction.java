/**
 *  Provides references to possible directions 
 */
public enum Direction {
 /**
  * Direction to move North or (0,+1) 
 */
 NORTH, 
 /**
  * Direction to move South or (0,-1) 
 */   
 SOUTH, 
 /**
  * Direction to move East or (+1,0) 
 */
 EAST, 
 /**
  * Direction to move West or (-1,0) 
 */   
 WEST, 
 /**
  * Direction to move nowhere, or stay at same place
 */   
 NONE;
}